foreach ($key in $videos.Keys) {
    $filepath = Join-Path $base_dir $key
    if (Test-Path $filepath) {
        $content = Get-Content -Path $filepath -Raw
        $index = $content.IndexOf("## Recommended Video Tutorials")
        if ($index -ge 0) {
            $content = $content.Substring(0, $index)
        }
        
        $new_section = "## Recommended Video Tutorials`n"
        $new_section += "Supplement this session with these excellent YouTube tutorials:`n`n"
        
        $i = 1
        foreach ($link in $videos[$key]) {
            $new_section += "$i. $link`n"
            $i++
        }
        
        $content += $new_section
        Set-Content -Path $filepath -Value $content
        Write-Host "Updated $filepath"
    }
}
